package buddy.common.constant.pool;

/**
 * 字符串和字符常量池
 *
 * @author anthony
 * @version 2024/09/11
 * @since 2023/10/19 13:56
 */
public interface CharSequencePool {
	char COMMA = ','; // 半角逗号
	char SEMICOLON = ';'; // 半角分号
	char EXCLAMATION_MARK = '!';  // 半角感叹号
	char DOT = '.'; // 半角点
	char SPACE = ' '; // 半角空格

	char DASHED_LINE = '-'; // 中划线
	char UNDERSCORE = '_'; // 下划线

	char AMPERSAND = '&';
	char AT = '@';
	char PER_CENT = '%';
}